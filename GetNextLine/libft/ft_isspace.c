#include "libft.h"

int	ft_isspace(int c)
{
	if (c == 0x20 || c == 0x90 || c == 0x0a || c == (0x0b) ||
			c == (0x0c) || c == (0x0d) || c == '\t')
		return (c);
	else
		return (0);
}
